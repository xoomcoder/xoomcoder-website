        <h2>Programmation Orienté-Objet</h2>
        <pre>

Naturellement, pour pouvoir gérer des projets avec de tels volumes de code, il faut bien s'organiser. Une entreprise attendra donc des équipes de développeurs que leur code soit bien organisé. 

🔥 Divide and Conquer.

C'est un principe ancien et connu des humains depuis des milliers d'années.
🔥 En français: on pourrait traduire "Divide and Conquer" par "Séparer et ensuite Rassembler".

Pour déplacer une montagne, on peut découper des blocs, et ensuite déplacer ces blocs pour les rassembler ailleurs.
🌞 C'est comme ça que les égyptiens ont réussi à construire des pyramides qui existent toujours. Dans le monde informatique, on est heureusement dans du virtuel, ça pèse moins lourd que des blocs de pierre.

On va appliquer le même principe: on découpe tout le programme en blocs.

🔥 La programmation fonctionnelle.

La fonction, c'est le premier bloc fondamental du développeur.
Pour organiser votre code, vous allez regrouper des blocs de code dans des fonctions.

Si on s'organise avec comme ordre de grandeur 100.
100 lignes de code rangées dans 100 fonctions... 
Cela donne 10000 lignes de code.
On est encore loin du besoin des entreprises actuelles.
La programmation fonctionnelle était suffisante dans les années 80.

🔥 La programmation orientée objet (POO).

Si on travaille sur un projet qui a des centaines de fonctions, on va aussi avoir besoin d'organiser les fonctions.
La programmation orienté-objet permet de regrouper les fonctions dans des classes.
100 classes x 100 fonctions x 100 lignes de code... 
😇 Voilà on arrive à 1 million de lignes de code ! 
Depuis les années 90, la programmation orienté objet est devenu la technique standard dans les entreprises pour réaliser des projets en équipe.

🔥 Vers l'infini et au delà ?

L'informatique est tellement répandue dans le monde actuel et il y a des entreprises qui gèrent des projets avec des volumes de code immenses.
Oui, c'est déjà prévu de pouvoir ranger les classes dans des boites en plus...
La plupart des langages orienté-objet ont déjà prévu une boite dans laquelle on pourra ranger les classes. Dans le monde Java, on va parler de "package", dans le monde PHP, on va parler de "namespace"...
Ce sont les années 2000 qui ont vu ce besoin arriver.

Depuis, la tendance est de simplifier les langages et d'apporter de meilleurs outils aux développeurs. Les projets avec des millions de lignes de code sont difficiles à gérer pour la plupart des équipes.
Les entreprises vont préférer découper les projets en plusieurs applications indépendantes qui interagissent par des API.

En conclusion, la Programmation Orienté-Objet (POO) est une technique de développement qui a fait ses preuves depuis plusieurs décennies.
Et actuellement, la POO reste toujours le standard de développement pour des projets en équipe.
🔥 Si vous avez envie de travailler dans une équipe de développeurs, la POO est une technique incontournable.
🔥 C'est un des objectifs principaux pour une formation de développeur junior.

