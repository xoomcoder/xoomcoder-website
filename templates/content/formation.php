        <h2>Développeur Junior</h2>
        <pre>

Vous êtes motivés par un emploi de Développeur Junior ? 
Mais savez-vous ce qu'est le métier de développeur ?
Un développeur passe sa journée de travail devant un écran d'ordinateur et écrit des lignes de code. 

❓ Et combien un Développeur écrit de lignes de code ?
Les chiffres publiés donnent environ 400 lignes de code par jour.

Si vous êtes aux 35 heures, ça veut dire 7H par jour, soit 7x60=420 minutes de travail journalier.
😇 Cela donnerait moins d'une ligne de code par minute ?! 
Si vous avez déjà écrit une ligne de code, vous savez que vous n'avez pas besoin d'une minute pour taper cette ligne sur un clavier.
En réalité, avant d'écrire votre ligne de code, vous allez y réfléchir d'abord. 
Et après avoir écrit la ligne de code, vous allez vérifier qu'elle fait ce que vous attendez.

Voilà une journée typique de développeur: 
🔥 Vous réfléchissez (...et vous cherchez beaucoup sur internet).
🔥 Vous écrivez votre code.
🔥 Vous vérifiez que le programme fonctionne correctement.

La routine du quotidien passe... 
🔥 En une semaine, on obtient 400x5, soit 2000 lignes de code ! 
😓 Disons que vous travaillez 50 semaines par an... 
🔥 50x2000 lignes, cela donne environ 100000 lignes de code par an ?! 

Si vous vous placez du point de vue d'une entreprise, vous savez qu'un développeur pourra produire autour de 100000 lignes de code par an.
Et un développeur junior aspire à intégrer une entreprise qui emploie une équipe de plusieurs développeurs.
Pour avoir besoin d'autant de développeurs, le projet va contenir des centaines de milliers de lignes de code, peut-être des millions ?

Voilà le contexte de travail d'un développeur professionnel.

🔥 Il faut pouvoir prendre en main des centaines de milliers de lignes de code. 
🔥 Il faut pouvoir travailler dans une équipe de plusieurs développeurs.

😇 Imaginons que pour un poste de développeur, une entreprise consacre comme budget 50000 euros par an et que le développeur produit 100000 lignes de code par an. 
💰 Cela donne un prix de 50 centimes la ligne de code ?! 

Actuellement, la plupart des équipes de développement se composent de 4 à 8 personnes.

